    The college started functioning in September 13, 1995 as a self financing institution with 20 students each on the
    rolls in the undergraduate courses of B.Sc. Polymer Chemistry, B.Sc. Electronics and B.Sc. Computer Science.
    The first batch of “Mar Thoma College graduates” rolled out in 1998, what we call as the ‘academic baptism’ of the
    college. In 2001, a new Career-related course titled B.Sc. Botany and Biotechnology was introduced. B.Com. with
    Computer Applications was added to the list in 2005.
    The National Commission for Minority Educational Institutions, Govt. of India vide its order F.No. 799 of 2009/38049
    granted Minority Status Certification to the college on September 24, 2009. In the academic year 2013-14, B.B.A. and
    B.A. English Language and Literature courses were also started.
    In addition to these, five PG Programs were sanctioned by the University from 2005 to 2020, elevating its status to
    that of a Post graduate College. MA English course started in October, 2020 .
    Thus in a short span of time the college has developed into a sprawling campus with state of the art amenities. Now
    the college has, seven under-graduate and six post-graduate courses with due affiliation from the University of
    Kerala.
    This transformation is truly remarkable and astonishing thanks to His abundant mercy and blessings and the visionary
    excellence of His Grace The Most Rev. Dr. Joseph Mar Thoma Metropolitan, our founding father. The college is
    undoubtedly the fruit of the unbending resolve, intrepid bravery and venturesome boldness of His Grace the
    Metropolitan. While celebrating our twentieth year we proudly salute the wisdom, vision and passion of our founding
    father. With a deep sense of gratitude we remember all those who worked in various capacities and contributed to
    building the institution to this level. We strongly believe that a literate and educated citizenry is vital to a
